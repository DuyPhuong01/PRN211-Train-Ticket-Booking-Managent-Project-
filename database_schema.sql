CREATE TABLE [ticket_types] (
  [type_id] int PRIMARY KEY NOT NULL,
  [type_name] nvarchar(128)
)
GO

CREATE TABLE [ticket_classes] (
  [class_id] int PRIMARY KEY NOT NULL,
  [class_name] nvarchar(128)
)
GO

